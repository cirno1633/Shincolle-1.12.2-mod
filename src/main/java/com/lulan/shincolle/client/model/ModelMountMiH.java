    @Override
    public void render(Entity entity, float f, float f1, float f2, float f3, float f4, float f5)
    {
    	//FIX: head rotation bug while riding
    	if (f3 <= -180F) { f3 += 360F; }
    	else if (f3 >= 180F) { f3 -= 360F; }
    	
    	GlStateManager.pushMatrix();
    	GlStateManager.enableBlend();
    	GlStateManager.blendFunc(SourceFactor.SRC_ALPHA, DestFactor.ONE_MINUS_SRC_ALPHA);
    	GlStateManager.scale(0.97F , 0.97F, 0.97F);
    	GlStateManager.translate(0F, 0.08F, 0F);
    	
    	//main body
    	setRotationAngles(f, f1, f2, f3, f4, f5, entity);
    	this.BodyMain.render(f5);
    	GlStateManager.disableBlend();
    	
    	//light part
    	GlStateManager.disableLighting();
    	GlStateManager.enableCull();
    	OpenGlHelper.setLightmapTextureCoords(OpenGlHelper.lightmapTexUnit, 240F, 240F);
    	this.GlowBodyMain.render(f5);
    	GlStateManager.disableCull();
    	GlStateManager.enableLighting();
    	
    	GlStateManager.popMatrix();
    }
    
    //for idle/run animation
    @Override
    public void setRotationAngles(float f, float f1, float f2, float f3, float f4, float f5, Entity entity)
    {
		super.setRotationAngles(f, f1, f2, f3, f4, f5, entity);
		
		IShipEmotion ent = (IShipEmotion)entity;
		  
		motionHumanPos(f, f1, f2, f3, f4, ent);
    }
    
    //雙腳移動計算
  	private void motionHumanPos(float f, float f1, float f2, float f3, float f4, IShipEmotion ent)
  	{   
  		float angleX = MathHelper.cos(f2 * 0.08F);
  		float angleX2 = MathHelper.cos(f2 * 0.5F + 0.3F);
  		float angleX3 = MathHelper.cos(f2 * 0.5F + 0.6F);
  		float angleX4 = MathHelper.cos(f2 * 0.5F + 0.9F);
  		float angleAdd1 = MathHelper.cos(f * 0.7F) * f1 * 0.7F;
  		float angleAdd2 = MathHelper.cos(f * 0.7F + 3.1415927F) * f1 * 0.7F;
  		float addk1 = angleAdd1 * 0.5F;
  		float addk2 = angleAdd2 * 0.5F;
  		
  		//水上漂浮
  		if (ent.getShipDepth(0) > 0D)
  		{
  			GlStateManager.translate(0F, angleX * 0.015F + 0.025F, 0F);
    	}

  		//body
  		this.UpperMain.rotateAngleX = -0.46F;
  		//leg
  		this.LegArmorA1.rotateAngleY = addk1 + 0.35F;
  		this.LegArmorA2.rotateAngleY = addk1 + 0F;
  		this.LegArmorA3.rotateAngleY = addk2 + 0F;
  		this.LegArmorA4.rotateAngleY = addk2 - 0.68F;
  		this.LegArmorA1.offsetY = angleX * 0.1F + angleAdd1 * 0.3F;
  		this.LegArmorA2.offsetY = -angleX * 0.1F + angleAdd1 * 0.3F;
  		this.LegArmorA3.offsetY = -angleX * 0.1F + angleAdd2 * 0.2F;
  		this.LegArmorA4.offsetY = angleX * 0.1F + angleAdd2 * 0.2F;
	    //idle
	  	this.TongueBase1.rotateAngleX = angleX2 * 0.05F - 0.61F;
	  	this.TongueBase2.rotateAngleX = -angleX3 * 0.08F + 0.61F;
	  	this.TongueBase3.rotateAngleX = -angleX4 * 0.05F + 0.61F;
	    //cannon
	  	this.TopCannonBase.rotateAngleY = f3 * 0.008F;
	  	this.TopCannonBase_1.rotateAngleY = f3 * 0.008F;
	    this.TopCannon01b.rotateAngleX = f4 * 0.014F - 0.3F;
	    this.TopCannon01b_1.rotateAngleX = f4 * 0.014F - 0.25F;
	    this.TopCannon01b_2.rotateAngleX = f4 * 0.014F - 0.35F;
	    this.TopCannon01b_3.rotateAngleX = f4 * 0.014F - 0.15F;
	    this.TopCannon01b_4.rotateAngleX = f4 * 0.014F - 0.2F;
	    this.TopCannon01b_5.rotateAngleX = f4 * 0.014F - 0.1F;
	    
	    //sitting
	    if (ent.getIsSitting())
	    {
	    	this.TongueBase1.rotateAngleX = angleX2 * 0.025F - 0.41F;
		  	this.TongueBase2.rotateAngleX = -angleX3 * 0.04F + 0.41F;
		  	this.TongueBase3.rotateAngleX = -angleX4 * 0.025F + 0.71F;
	    	this.UpperMain.rotateAngleX = -0.15F;
	    }
	    
	    //sync rotate
	    this.GlowBodyMain.rotateAngleX = this.BodyMain.rotateAngleX;
	    this.GlowTopCannonBase.rotateAngleY = this.TopCannonBase.rotateAngleY;
	    this.GlowTopCannonBase_1.rotateAngleY = this.TopCannonBase_1.rotateAngleY;
  	}
  	

